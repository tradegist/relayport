"""Generate the Python types package from shared and relay model files.

Mirrors the relay_core source structure into types/python/relayport_types/:

  Source                                  Generated
  ──────────────────────────────────────  ─────────────────────────────────────
  services/shared/models.py            →  relayport_types/shared.py
  services/relay_core/relay_models.py  →  relayport_types/relay_api.py
  relay_core/notifier/models.py        →  relayport_types/notifier/models.py

Each file is copied verbatim (docstring stripped, header prepended) with one
import rewrite to fix the relative depth:

  shared.py          : from shared import  →  from .shared import
  relay_api.py       : from shared import  →  from .shared import
                       from .notifier …    →  (unchanged — path stays valid)
  notifier/models.py : from shared import  →  from ..shared import

The top-level ``__init__.py`` and the ``notifier/__init__.py`` barrels
are also regenerated. Re-export lists are derived by AST-walking each
source for top-level ``class`` and assignment definitions — no manual
list to keep in sync.

Usage: python gen_python_types.py
"""

import ast
import pathlib
import re
import sys

SHARED_SRC = pathlib.Path("services/shared/models.py")
RELAY_API_SRC = pathlib.Path("services/relay_core/relay_models.py")
NOTIFIER_MODELS_SRC = pathlib.Path("services/relay_core/notifier/models.py")
MARKET_DATA_API_SRC = pathlib.Path("services/market_data/models/dividends.py")
OUT_DIR = pathlib.Path("types/python/relayport_types")

SHARED_HEADER = '''\
"""relayport_types shared types — CommonFill models and type aliases.

AUTO-GENERATED by gen_python_types.py from services/shared/models.py.
Do not edit manually — run ``make types`` to regenerate.
"""
'''

RELAY_API_HEADER = '''\
"""relayport_types relay API response models.

AUTO-GENERATED by gen_python_types.py from services/relay_core/relay_models.py.
Do not edit manually — run ``make types`` to regenerate.
"""
'''

NOTIFIER_MODELS_HEADER = '''\
"""relayport_types notifier payload contracts.

AUTO-GENERATED by gen_python_types.py from services/relay_core/notifier/models.py.
Do not edit manually — run ``make types`` to regenerate.
"""
'''

MARKET_DATA_API_HEADER = '''\
"""relayport_types market data API request/response models.

AUTO-GENERATED by gen_python_types.py from services/market_data/models/dividends.py.
Do not edit manually — run ``make types`` to regenerate.
"""
'''

INIT_HEADER = '''\
"""relayport_types — Pydantic models for RelayPort.

AUTO-GENERATED by gen_python_types.py from services/shared/models.py +
services/relay_core/relay_models.py + services/relay_core/notifier/models.py.
Do not edit manually — run ``make types`` to regenerate.
"""
'''


def _strip_docstring(source: str) -> str:
    if source.startswith('"""'):
        end = source.find('"""', 3)
        if end != -1:
            return source[end + 3:].lstrip("\n")
    return source


def _rewrite(source: str, pattern: str, replacement: str) -> str:
    return re.sub(pattern, replacement, source, flags=re.MULTILINE)


def _copy(src: pathlib.Path, out: pathlib.Path, header: str, rewrites: list[tuple[str, str]]) -> None:
    if not src.exists():
        print(f"ERROR: {src} not found", file=sys.stderr)
        sys.exit(1)
    body = _strip_docstring(src.read_text())
    for pattern, replacement in rewrites:
        body = _rewrite(body, pattern, replacement)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(header + "\n" + body)
    print(f"Generated {out}")


def _collect_public_names(src: pathlib.Path) -> list[str]:
    """Walk *src*'s AST for top-level public class/assign/annassign names.

    Returns alphabetically sorted names with a leading ``_`` filtered.
    """
    tree = ast.parse(src.read_text())
    names: set[str] = set()
    for node in tree.body:
        if isinstance(node, ast.ClassDef):
            names.add(node.name)
        elif isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
            names.add(node.target.id)
        elif isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name):
                    names.add(target.id)
    return sorted(n for n in names if not n.startswith("_"))


def generate_shared() -> None:
    _copy(
        SHARED_SRC,
        OUT_DIR / "shared.py",
        SHARED_HEADER,
        [(r"^from shared import ", "from .shared import ")],
    )


def generate_relay_api() -> None:
    _copy(
        RELAY_API_SRC,
        OUT_DIR / "relay_api.py",
        RELAY_API_HEADER,
        [
            # RelayName is only imported in the source for schema_gen hoisting;
            # it is unused in the types package. Strip it so the generated file
            # stays lint-clean without needing a noqa suppression.
            (r"^from shared import RelayName, Trade.*\n", "from shared import Trade\n"),
            (r"^from shared import ", "from .shared import "),
            # from .notifier.models import … is unchanged — it resolves correctly
            # in the mirrored package structure.
        ],
    )


def generate_notifier_models() -> None:
    _copy(
        NOTIFIER_MODELS_SRC,
        OUT_DIR / "notifier" / "models.py",
        NOTIFIER_MODELS_HEADER,
        [(r"^from shared import ", "from ..shared import ")],
    )


def generate_notifier_init() -> None:
    out = OUT_DIR / "notifier" / "__init__.py"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("")
    print(f"Generated {out}")


def generate_market_data_api() -> None:
    _copy(
        MARKET_DATA_API_SRC,
        OUT_DIR / "market_data_api.py",
        MARKET_DATA_API_HEADER,
        [],  # no import rewrites needed — only pydantic imports
    )


def generate_init() -> None:
    """Regenerate the top-level ``__init__.py`` barrel.

    Walks each source module for public top-level names and emits one
    ``from .<module> import X as X`` line per name.
    """
    notifier_names = _collect_public_names(NOTIFIER_MODELS_SRC)
    relay_api_names = _collect_public_names(RELAY_API_SRC)
    shared_names = _collect_public_names(SHARED_SRC)
    market_data_api_names = _collect_public_names(MARKET_DATA_API_SRC)

    # relay_api.py re-exports notifier symbols (for schema_gen) — drop them
    # from the relay_api barrel section to avoid duplicate imports.
    relay_api_names = [n for n in relay_api_names if n not in notifier_names]

    lines = [INIT_HEADER, ""]
    for symbol in market_data_api_names:
        lines.append(f"from .market_data_api import {symbol} as {symbol}")
    for symbol in notifier_names:
        lines.append(f"from .notifier.models import {symbol} as {symbol}")
    for symbol in relay_api_names:
        lines.append(f"from .relay_api import {symbol} as {symbol}")
    for symbol in shared_names:
        lines.append(f"from .shared import {symbol} as {symbol}")
    lines.append("")

    out = OUT_DIR / "__init__.py"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(lines))
    total = len(market_data_api_names) + len(notifier_names) + len(relay_api_names) + len(shared_names)
    print(f"Generated {out} ({total} exports)")


if __name__ == "__main__":
    generate_shared()
    generate_relay_api()
    generate_notifier_models()
    generate_notifier_init()
    generate_market_data_api()
    generate_init()
